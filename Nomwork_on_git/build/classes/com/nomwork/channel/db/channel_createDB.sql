DROP 	SEQUENCE CHANNEL_CREATENOSEQ;
CREATE 	SEQUENCE CHANNEL_CREATENOSEQ;

CREATE TABLE CHANNEL_CREATE(
	CHANNEL_CREATENO 	NUMBER 	PRIMARY KEY
	,USERNO 			NUMBER
	,CHANNELNO 			NUMBER
	,PROJECTNO 			NUMBER
	,CONSTRAINT FK_CHANNEL_CREATE_USERNO 	FOREIGN KEY(USERNO) 	REFERENCES USERINFO(USERNO)
	,CONSTRAINT FK_CHANNEL_CREATE_CHANELNO 	FOREIGN KEY(CHANNELNO) 	REFERENCES CHANNEL(CHANNELNO)
	,CONSTRAINT FK_CHANNEL_CREATE_PROJECTNO FOREIGN KEY(PROJECTNO) 	REFERENCES PROJECT(PROJECTNO)
);