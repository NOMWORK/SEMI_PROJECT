DROP SEQUENCE USERNOSEQ;
CREATE SEQUENCE USERNOSEQ;
DROP TABLE USERINFO;

CREATE TABLE USERINFO(
    USERNO      NUMBER          PRIMARY KEY
    ,USEREMAIL    VARCHAR2(50)
    ,USERPW      VARCHAR2(20)   NOT NULL
    ,USERNAME    VARCHAR2(20)   NOT NULL
    ,USERGENDER  VARCHAR2(1)
    ,USERENABLED VARCHAR2(1)
    ,USERROLE    VARCHAR2(20)   NOT NULL
    ,USERURL VARCHAR2(3000)
    ,CONSTRAINT USERIFNO_USERGENDER_CK  CHECK (USERGENDER IN('M', 'W'))
    ,CONSTRAINT USERINFO_USERENABLED_CK CHECK (USERENABLED IN('Y', 'N'))
    ,CONSTRAINT USERINFO_USERROLE_CK    CHECK (USERROLE IN('1', '2')));